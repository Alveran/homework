#include <stdio.h>

int main() {
  int a, b; // Vereinbarungen
  
  // \n == newline
  printf("\n\n Hallo TUTOR! \n\n");
  a = 22; // Anweisungen 
  b = 20; // "
  // Ist das nicht immer die Antwort?
  // \t == tab %d decimal, wird dem Ergebnis von dem Term
  // a + b zugewiesen.
  printf("Die Antwort ist: \t %d \n\n", a + b);
  
  // Beendet diese Methode/Funktion
  return 0;
}  // Ende der Main methode und des Programms
